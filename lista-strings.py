my_string = "Curso de Código Facilito!"

print(my_string)

#Con números negativos se lee inverso
print(my_string[-1])

#Un string basado en otro string
#igual se puede con negativos
print(my_string[0:10])
print(my_string[-2:-1])

#Saltos progresivos
print(my_string[0:10:2])

#Leer de derecha a izquierda(reverse)
print(my_string[::-1])